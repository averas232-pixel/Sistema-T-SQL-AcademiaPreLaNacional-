CREATE DATABASE AcademiaPreLaNacional;

USE AcademiaPreLaNacional;
GO

CREATE TABLE PERSONA (
    DNI CHAR(8) PRIMARY KEY,
    Nombre VARCHAR(40) NOT NULL,
    Apellido_Paterno VARCHAR(30) NOT NULL,
    Apellido_Materno VARCHAR(30) NOT NULL,
    Telefono CHAR(9),
    Gmail VARCHAR(40),
    Fecha_Nacimiento DATE
);

CREATE TABLE SUCURSAL (
    Codigo_sucursal VARCHAR(10) PRIMARY KEY,
    Nombre VARCHAR(30),
    Direccion_Calle VARCHAR(40),
    Direccion_Numero INT,
    Telefono CHAR(9),
    Cantidad_aulas INT,
    Empleados_Asignados INT
);

CREATE TABLE CLIENTE (
    DNI_Persona CHAR(8) PRIMARY KEY,
    Colegio_procedencia VARCHAR(40),
    Lugar_procedencia VARCHAR(40),
    Tipo_colegio VARCHAR(20),
    Carrera_postula VARCHAR(30),
    FOREIGN KEY (DNI_Persona) REFERENCES PERSONA(DNI)
);

CREATE TABLE EMPLEADO (
    DNI_Persona CHAR(8) PRIMARY KEY,
    Sueldo DECIMAL(10,2),
    Anios_Experiencia INT,
    Fecha_Contratacion DATE,
    Sucursal_Asignada VARCHAR(30),
    Tipo_Empleado VARCHAR(30),
    FOREIGN KEY (DNI_Persona) REFERENCES PERSONA(DNI)
);

CREATE TABLE DOCENTE (
    DNI_Empleado CHAR(8) PRIMARY KEY,
    Especialidad VARCHAR(40),
    Grado_Academico VARCHAR(40),
    Certificacion1 VARCHAR(30),
    Certificacion2 VARCHAR(30),
    Certificacion3 VARCHAR(30),
    Materia1 VARCHAR(30),
    Materia2 VARCHAR(30),
    Materia3 VARCHAR(30),
    FOREIGN KEY (DNI_Empleado) REFERENCES EMPLEADO(DNI_Persona)
);

CREATE TABLE ADMINISTRATIVO (
    DNI_Empleado CHAR(8) PRIMARY KEY,
    Area_Trabajo VARCHAR(50),
    Funcion1 VARCHAR(30),
    Funcion2 VARCHAR(30),
    Funcion3 VARCHAR(30),
    FOREIGN KEY (DNI_Empleado) REFERENCES EMPLEADO(DNI_Persona)
);

CREATE TABLE LIMPIEZA (
    DNI_Empleado CHAR(8) PRIMARY KEY,
    Turno VARCHAR(20),
    FOREIGN KEY (DNI_Empleado) REFERENCES EMPLEADO(DNI_Persona)
);

CREATE TABLE SECRETARIA (
    DNI_Administrativo CHAR(8) PRIMARY KEY,
    FOREIGN KEY (DNI_Administrativo) REFERENCES ADMINISTRATIVO(DNI_Empleado)
);

CREATE TABLE JEFE (
    DNI_Administrativo CHAR(8) PRIMARY KEY,
    Fecha_asignacion DATE,
    FOREIGN KEY (DNI_Administrativo) REFERENCES ADMINISTRATIVO(DNI_Empleado)
);

CREATE TABLE INVENTARIO (
    Codigo_Inventario VARCHAR(10) PRIMARY KEY,
    Descripcion VARCHAR(150),
    Cantidad INT,
    Fecha_Actualizacion DATE,
    Cod_Sucursal VARCHAR(10),
    FOREIGN KEY (Cod_Sucursal) REFERENCES SUCURSAL(Codigo_sucursal)
);

CREATE TABLE AULA (
    Cod_Aula VARCHAR(10) PRIMARY KEY,
    Numero_Aula INT,
    Capacidad INT,
    Estado_Aula VARCHAR(20),
    Cod_Inventario VARCHAR(10),
    Cod_Sucursal VARCHAR(10),
    FOREIGN KEY (Cod_Inventario) REFERENCES INVENTARIO(Codigo_Inventario),
    FOREIGN KEY (Cod_Sucursal) REFERENCES SUCURSAL(Codigo_sucursal)
);

CREATE TABLE CURSO (
    Codigo_Curso VARCHAR(10) PRIMARY KEY,
    DNI_Docente CHAR(8),
    Nombre VARCHAR(30),
    Descripcion VARCHAR(100),
    Fecha_Inicio DATE,
    Fecha_fin DATE,
    FOREIGN KEY (DNI_Docente) REFERENCES DOCENTE(DNI_Empleado)
);

CREATE TABLE MATERIAL (
    Codigo_material VARCHAR(10) PRIMARY KEY,
    Autor VARCHAR(50),
    Titulo VARCHAR(40),
    Tipo_material VARCHAR(30),
    Descripcion VARCHAR(100),
    Cod_Curso VARCHAR(10),
    Cod_Inventario VARCHAR(10),
    FOREIGN KEY (Cod_Curso) REFERENCES CURSO(Codigo_Curso),
    FOREIGN KEY (Cod_Inventario) REFERENCES INVENTARIO(Codigo_Inventario)
);

CREATE TABLE PAGO (
    Id_pago INT IDENTITY(1,1) PRIMARY KEY,
    Monto DECIMAL(10,2),
    Tipo_Pago VARCHAR(30),
    Fecha DATETIME,
    Estado_Pago BIT,
    DNI_Cliente CHAR(8),
    DNI_Secretaria CHAR(8),
    FOREIGN KEY (DNI_Cliente) REFERENCES CLIENTE(DNI_Persona),
    FOREIGN KEY (DNI_Secretaria) REFERENCES SECRETARIA(DNI_Administrativo)
);

CREATE TABLE INSCRIPCION (
    Codigo_inscripcion VARCHAR(10) PRIMARY KEY,
    DNI_Cliente CHAR(8),
    DNI_Secretaria CHAR(8),
    Fecha_inscripcion DATE,
    FOREIGN KEY (DNI_Cliente) REFERENCES CLIENTE(DNI_Persona),
    FOREIGN KEY (DNI_Secretaria) REFERENCES SECRETARIA(DNI_Administrativo)
);

CREATE TABLE ASISTENCIA (
    Cod_Asistencia VARCHAR(10) PRIMARY KEY,
    DNI_Cliente CHAR(8),
    DNI_Secretaria CHAR(8),
    Hora_Entrada DATETIME,
    Hora_Salida DATETIME,
    FOREIGN KEY (DNI_Cliente) REFERENCES CLIENTE(DNI_Persona),
    FOREIGN KEY (DNI_Secretaria) REFERENCES SECRETARIA(DNI_Administrativo)
);

CREATE TABLE RECLAMO (
    Cod_Reclamo VARCHAR(10) PRIMARY KEY,
    Fecha_Reclamo DATE,
    Tipo_Reclamo VARCHAR(30),
    Descripcion VARCHAR(200),
    Estado_Reclamo BIT,
    DNI_Cliente CHAR(8),
    DNI_Jefe CHAR(8),
    FOREIGN KEY (DNI_Cliente) REFERENCES CLIENTE(DNI_Persona),
    FOREIGN KEY (DNI_Jefe) REFERENCES JEFE(DNI_Administrativo)
);

CREATE TABLE PLANILLA (
    Cod_Planilla VARCHAR(10) PRIMARY KEY,
    Cod_Empleado CHAR(8),
    Salario_Bruto DECIMAL(10,2),
    Descuento DECIMAL(10,2),
    Bonificaciones DECIMAL(10,2),
    Salario_Neto AS (Salario_Bruto - Descuento + Bonificaciones),
    Fecha_Pago DATE,
    FOREIGN KEY (Cod_Empleado) REFERENCES EMPLEADO(DNI_Persona)
);

CREATE TABLE EVALUACION (
    Id_Evaluacion     INT IDENTITY(1,1) PRIMARY KEY,         
    Codigo_evaluacion VARCHAR(10)    NOT NULL,                 
    DNI_Cliente       CHAR(8)        NOT NULL,
    Modulo            VARCHAR(30)   NOT NULL,
    Fecha_evaluacion  DATE           NOT NULL,
    Puntaje           DECIMAL(5,2)   NOT NULL,
    
    CONSTRAINT UQ_Evaluacion_Alumno_Codigo 
        UNIQUE (Codigo_evaluacion, DNI_Cliente),
    
    FOREIGN KEY (DNI_Cliente) REFERENCES CLIENTE(DNI_Persona)
);
GO

CREATE TABLE DEPENDIENTE (
    DNI_Dependiente CHAR(8) PRIMARY KEY,
    DNI_Empleado CHAR(8),
    Parentesco VARCHAR(30),
    Nombre VARCHAR(50),
    Telefono CHAR(9),
    Direccion_Calle VARCHAR(50),
    Direccion_Numero INT,
    FOREIGN KEY (DNI_Empleado) REFERENCES EMPLEADO(DNI_Persona)
);
GO

